/* Gerando número aleatórios 
 * 
 * A classe Random do namespace System é usada para gerar números pseudo-aleatórios com base
 * no valor de uma semente(seed)
 * 
 * Por padrão, o construtor sem parâmetros da classe Random usa o relógio do sistema para gerar
 * o valor de semente.
 * 
 * A geração de números aleatórios começa a partir de um valor de semente (seed) e se a mesma semente
 * for usada repetidamente, a mesma série de números será gerada.
 */