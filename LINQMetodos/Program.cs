/* LINQ - Expressão lambda 
 * 
 * Uma expressão lambda é um método anônimo que você pode usar para criar delegates ou tipos de árvore de expressão 
 * 
 * Usando expressões lambda, você pode gravar métodos locais que podem ser passados como argumentos ou serem retornados
 * como o valor de chamadas de método
 */


/* LINQ - Expressão lambda - Sintaxe 
 * 
 * Uma expressão lambda tem um lado esquerdo e um lado direito, separados pelo operador lambda '=>' (significa 'vá para')
 * Ex: x => x * x
 * 
 * No lado esquerdo temos o(s) parâmetro(s) de entrada:(x)
 * O lado direito trata a expressão ou bloco de instruções e gera o resultado esperado (x*x)
